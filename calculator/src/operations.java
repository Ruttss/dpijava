public class operations {
    public static float plus(float a, float b) {
        return a + b;
    }

    public static float minus(float a, float b) {
        return a - b;
    }

    public static float umnoj(float a, float b) {
        return a * b;
    }

    public static float del(float a, float b) {
        return a / b;
    }
}
